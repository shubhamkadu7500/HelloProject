
public class Test2 {
	
	public static void main(String args[]) {
		int number = 20;
		
		System.out.println(number++);//20, 21
		System.out.println(number);//
		System.out.println(++number);//22
		
		System.out.println(number--);//22,21
		System.out.println(number);//
		System.out.println(--number);//20
	}

}
